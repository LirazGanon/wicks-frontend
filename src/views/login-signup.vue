<template>
  <div class="container about">
    <p>{{ msg }}</p>

    <div v-if="loggedinUser">
      <h3>
        Loggedin User:
        {{ loggedinUser.fullname }}
        <button @click="doLogout">Logout</button>
      </h3>
    </div>
    <div v-else>
      <section v-if="!isLogin">

        <h2>Login</h2>
        <form @submit.prevent="doLogin">
          <input type="text" v-model="loginCred.username" placeholder="puki">
          <input type="text" v-model="loginCred.password" placeholder="ja">


          <button>Login</button>
        </form>
        <p class="mute">user1 or admin, pass:123 </p>
      </section>
      <section v-if="isLogin">
        <form @submit.prevent="doSignup">
          <h2>Signup</h2>
          <input type="text" v-model="signupCred.fullname" placeholder="Your full name" />
          <input type="text" v-model="signupCred.password" placeholder="Password" />
          <input type="text" v-model="signupCred.username" placeholder="Username" />
          <img-uploader @uploaded="onUploaded"></img-uploader>
          <button>Signup</button>
        </form>
      </section>
    </div>
    <hr />
    <details>
      <summary>
        Admin Section
      </summary>
      <ul>
        <li v-for="user in users" :key="user._id">
          <pre>{{ user }}</pre>
          <button @click="removeUser(user._id)">x</button>
        </li>
      </ul>
    </details>
  </div>
</template>

<script>

import imgUploader from '../cmps/img-uploader.vue'

export default {
  name: 'login-signup',
  data() {
    return {
      msg: '',
      loginCred: { username: '', password: '' },
      signupCred: { username: '', password: '', fullname: '', imgUrl: '' },
      isLogin: false
    }
  },
  computed: {
    users() {
      return this.$store.getters.users
    },
    loggedinUser() {
      return this.$store.getters.loggedinUser
    },
  },
  created() {
    this.loadUsers()
  },
  methods: {
    async doLogin() {

      // TODO:MAKE IT ASSENTIAL TO FILL THE INPUT AND GET RID OF THIS CONDITION
      if (!this.loginCred.username || !this.loginCred.password) {
        this.msg = 'Please enter username/password'
        return
      }
      try {
        await this.$store.dispatch({ type: "login", userCred: this.loginCred })
        console.log(this.loggedinUser)
        const loggedinUserId = this.loggedinUser._id
        this.$router.push(`/user/${loggedinUserId}`)
      } catch (err) {
        console.log(err)
        this.msg = 'Failed to login'
      }
    },
    doLogout() {
      this.$store.dispatch({ type: 'logout' })
    },
    async doSignup() {
      if (!this.signupCred.fullname || !this.signupCred.password || !this.signupCred.username) {
        this.msg = 'Please fill up the form'
        return
      }
      await this.$store.dispatch({ type: 'signup', userCred: this.signupCred })
      // this.$router.push('/')

    },
    loadUsers() {
      this.$store.dispatch({ type: "loadUsers" })
    },
    async removeUser(userId) {
      try {
        await this.$store.dispatch({ type: "removeUser", userId })
        this.msg = 'User removed'
      } catch (err) {
        this.msg = 'Failed to remove user'
      }
    },
    onUploaded(imgUrl) {
      this.signupCred.imgUrl = imgUrl
    }

  },
  components: {
    imgUploader
  }
}
</script>